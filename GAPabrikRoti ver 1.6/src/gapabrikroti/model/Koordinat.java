package gapabrikroti.model;

/**
 *
 * @author cvgs
 */
public class Koordinat {
    private int id;
    private int x;
    private int y;
    private int bobot;
    private int XMax;
    private int YMax;
    private int XMin;
    private int YMin;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getBobot() {
        return bobot;
    }

    public void setBobot(int bobot) {
        this.bobot = bobot;
    }

    public int getXMax() {
        return XMax;
    }

    public void setXMax(int XMax) {
        this.XMax = XMax;
    }

    public int getYMax() {
        return YMax;
    }

    public void setYMax(int YMax) {
        this.YMax = YMax;
    }

    public int getXMin() {
        return XMin;
    }

    public void setXMin(int XMin) {
        this.XMin = XMin;
    }

    public int getYMin() {
        return YMin;
    }

    public void setYMin(int YMin) {
        this.YMin = YMin;
    }
    
    
}
