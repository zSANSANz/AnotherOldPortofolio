                            dd if=/dev/sdb | gzip > /usr/sdb.bak

                            Kita menggunakan dd, dengan input file /dev/sdb dan hasilnya di arahkan ke program gzip
                            untuk melakukan kompresi data dan disimpan di file /user/sdb.bak.
