                            a. Kegagalan Hardware
                            b. Salah Hapus
                            c. Pencurian
                            d. Virus

                            B. Backup dan Recovery di Sistem Linux Debian

                            Sistem Linux Debian juga diberi kemampuan untuk melakukan backup dan recovery untuk sistem.
                            Dengan aplikasi DD (Dataset Definition), bisa dilakukan sebuah backup penuh terhadap satu
                            disk yang langsung bisa ditaruh disk lainya atau diteruskan sebagai output yang nantinya
                            akan diproses menjadi sebuah file. Sebelum memulai backup, pilih disk yang akan di backup.
                            Untuk melihat daftar disk bisa dicari di direktori /dev.
                            Gunakan,

                            ls –l /dev | more

                            Untuk melihat isi dari direktori /dev secara rinci, dan juga membatasi outputnya supaya bisa
                            dibaca. Kegunaan dari operator | (pipa) adalah untuk mengarahkan output ke perintah di
                            sebelah kanan dari operator. Dalam kasus ini more, digunakan untuk melihat file sedikit demi
                            sedikit.
