                            A. Alasan Kenapa Backup Penting

                            Hilangnya file-file yang penting sangat mempengaruhi jalanya suatu kegiatan yang bergantung
                            terhadap file tersebut. Dengan backup, ketakutan akan kehilangan file tersebut sedikit
