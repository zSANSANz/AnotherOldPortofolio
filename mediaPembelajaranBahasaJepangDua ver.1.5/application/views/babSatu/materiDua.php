                            dituju. Salah satu program yang cukup powerfull dalam menangani masalah keterbatasan
                            kecepatan akses dan keamanan data yang dipergunakan untuk menangani keperluan internet
                            adalah SQUID. Bagi seseorang administrator jaringan,merupakan tugas utama untuk mengatur
                            lau-lintas data dalam suatu jaringan baik dari dalam maupun keluar jaringan. Squid merupakan
                            pilihan terbaik untuk membangun proxy server karena selain handal juga gratis dan mendukung
                            fitur ICP.
                            Melalui proxy server,seolah-olah klien merasaberinteraksi secara langsung dengan server
                            internet yang dituju.Kenyataannya,sebelum data atau permintaannya disampaikan ke tujuan,
                            data tersebut diarahkan terlebih dahulu ke server proxy,kemudian server proxy akan
                            meneruskan ke server yang dituju,yaitu internet.
                            Bila tidak menggunakan proxy maka ketika kita membuka atau mengkases suatu website harus
                            dengan melakukan query atau permintaan atau mengambil data secara langsung data yang berasal
                            dari tempat penyimpan data website tersebut.
                            Apabila kita browsing atau mengakses suatu website menggunakan proxy maka kita cukup
                            mengambil data website tersebut dari pihak Proxy tersebut yang selanjutnya proxylah yang
                            berperan mengambilkan data dari server suatu situs dan kemudian mengantarkan ke IP kita atau
                            sampai di Komputer kita.
